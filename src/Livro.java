
public class Livro {
	
	String codigo;
	String titulo;
	String autor;
	String anoPublicação;
	
	// CONSTRUTOR
	public Livro(String codigo, String titulo, String autor, String anoPublicação) {
		super();
		this.codigo = codigo;
		this.titulo = titulo;
		this.autor = autor;
		this.anoPublicação = anoPublicação;
	}
}
